    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());       
        try{
            String fullname = checkLogin(username, password);
            if(fullname!=null){
                JOptionPane.showMessageDialog(this, "Welcome " + fullname,"Welcome",1);
                new ManagerEmployees().setVisible(true);
                this.dispose();
                //this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(this, "Wrong \"User name or Password\"","Error",2);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Login Failed");
        }
        
        
        
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

